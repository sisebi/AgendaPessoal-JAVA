    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         if (editUsuario.getText() == null || editUsuario.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Campo Usuario deve ser preenchido !");
            return;
        }
        if (editSenha.getText() == null || editSenha.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Campo Senha deve ser preenchido !");
            return;
        }
        UsuarioControl controle = new UsuarioControl();              
        try {
            if (controle.validarUsuario(editUsuario.getText(),editSenha.getText())== true){                        
                Principal principal = new Principal();                
                principal.setUserName(controle); //Pega Usuario Logado   
                principal.setTitle(principal.getTitle()+" -- @"+principal.getUserName().getUserLogado().getUsuario());
                principal.setVisible(true);                
                TelaLogin.super.dispose();
//                JOptionPane.showMessageDialog(null, "teste mmmm "+principal.getUserName().getUserLogado().getUsuario());
            }else {
                JOptionPane.showMessageDialog(null,"Usuario ou Senha Errados !");
                return;
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Erro :"+ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    
